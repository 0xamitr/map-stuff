const MapComponent = () => {
  const mapRef = useRef(null);

  useEffect(() => {
    if (mapRef.current && typeof window !== 'undefined') {
      // Initialize the map
      const map = L.map(mapRef.current).setView([34, 80], 4);

      // Add OpenStreetMap tiles
      L.tileLayer('https://{s}.tile.openstreetmap.org/{z}/{x}/{y}.png', {
        maxZoom: 19,
        attribution: '&copy; <a href="https://openstreetmap.org/copyright">OpenStreetMap contributors</a>',
      }).addTo(map);

      // Function to style boundaries based on feature properties
      const boundaryStyle = (feature) => {
        const weight = map.getZoom() / 4;
        return {
          color: feature.properties.boundary === 'disputed' ? '#f2efea' : '#b9a8b9',
          weight: feature.properties.boundary === 'disputed' ? weight * 2 : weight,
        };
      };

      // Add the boundaries with styles
      const indiaBoundaries = L.geoJSON(indiaBoundaryLines, {
        style: boundaryStyle,
      }).addTo(map);

      // Update boundaries on zoom change
      map.on('zoomend', () => {
        indiaBoundaries.eachLayer((layer) => {
          layer.setStyle(boundaryStyle(layer.feature));
        });
      });

      // Cleanup on unmount
      return () => {
        map.remove();
      };
    }
  }, []);

  return <div ref={mapRef} style={{ height: '500px', width: '100%' }} />;
};

export default MapComponent;
